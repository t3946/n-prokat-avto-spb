            <h2>Что такое Подписка на автомобиль?</h2>
            <p>Программа подписки на авто это современный инструмент покупки автомобиля, при котором за ежемесячный платеж Вы получаете в пользование новый автомобиль с включенным полным обслуживанием, налогами, комплектом летней и зимней резины, страховкой КАСКО и ОСАГО.</p>
            <p>Вам остается оплачивать только бензин, все расходы по обслуживанию мы берем на себя! Спустя 6 месяцев долгосрочной аренды Вы можете переоформить договор на аренду с правом выкупа или заменить автомобиль на новый.</p>
            <h3>Какой автомобиль можно выбрать?</h3>
            <p>Мы предлагаем к аренде по подписке автомобили 2020-2021 гг. Все машины;</p>
            <ul>
                <li>Прошли техническое обслуживание;</li>
                <li>Новые, без пробега;</li>
                <li>В богатой комплектации;</li>
                <li>Допущены к выезду за границу (при наличии зеленой карты у арендатора);</li>
                <li>В идеальном состоянии (&laquo;из салона&raquo;).</li>
            </ul>
            <p>Наш автопарк постоянно пополняется новинками от Renault, Volkswagen, Hyundai и др. Следите за обновлениями!</p>
            <h3>За что Вы платите?</h3>
            <p>Стоимость подписки на автомобиль зависит от:</p>
            <ul>
                <li>Марки и модели машины;</li>
                <li>Длительности аренды;</li>
                <li>Предполагаемого пробега за месяц.</li>
            </ul>
            <p>Цены и требования к водителю указаны прямо на сайте. Мы работаем строго по договору и оформляем все документы. Водитель обязуется только соблюдать ПДД.</p>
            <p>Заправка машины осуществляется за счет арендатора. Никаких требований о содержании автомобиля только на стоянке нет.</p>
            <h3>За что платим мы?</h3>
            <p>Получая машину по подписке, Вы избавляетесь от огромного числа дополнительных плат и бюрократических сложности. Мы оплачиваем:</p>
            <ul>
                <li>Страховки;</li>
                <li>Ремонт и техническое обслуживание;</li>
                <li>Налоги;</li>
                <li>Зимнюю и летнюю резину.</li>
            </ul>
            <h3>Можно ли выкупить автомобиль?</h3>
            <p>Да, автомобиль, взятый по подписке, можно выкупить в собственность. Условия рассрочки и выкупа смотрите на сайте или уточняйте у менеджеров.</p>
            <h3>Как оформить подписку?</h3>
            <p>Взять машину по подписке может любой гражданин РФ, имеющий водительские права и стаж, указанный в требованиях к каждому автомобилю отдельно. Ценз по возрасту и стажу зависит от модели машины.</p>
            <p>Вы можете оформить подписку на себя или на другого человека. Мы работаем как с физическими, так и с юридическими лицами.</p>
            <p>Чтобы оформить подписку, нужно:</p>
            <ol>
                <li>Выбрать автомобиль через сайт или на стоянке.</li>
                <li>Выслать электронные копии водительских прав и документа, подтверждающего личность водителя. Для юридических лиц &ndash; реквизиты организации.</li>
                <li>Срок рассмотрения заявки составляет до 20 часов. После этого мы приглашаем Вас в офис для подписания документов и проведения испытаний машины.</li>
                <li>Наш механик при Вас проверяет выбранный автомобиль и передает документы на него.</li>
                <li>После внесения залога автомобиль переходит в Ваше распоряжение.</li>
            </ol>
            <p>Машина предоставляется только в частное пользование!</p>
            <p>Общую информацию о предлагаемых автомобилях и условиях подписки Вы можете получить на сайте, а на все возникшие вопросы Вам ответят наши менеджеры по телефону и в офисе.</p>
